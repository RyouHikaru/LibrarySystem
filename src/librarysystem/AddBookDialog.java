    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        String isbn = isbnField.getText();
        String title = titleField.getText();
        String copynum = copyField.getText();
        String author = authorField.getText();
        String shelf = shelfField.getSelectedItem().toString();
        String status = statusField.getSelectedItem().toString();
        String year = yearField.getText();
        String place = placeField.getText();
        
        if (validNumber(isbn) == false || validNumber(copynum) == false || validNumber(year) == false) {
            JOptionPane.showMessageDialog(rootPane, "ISBN no, Copy no, and  Year should be numeric", "Invalid numeric input", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        try {
            cst = con.prepareCall("{CALL addbookandwrittenwork(?,?,?,?,?,?,?,?)}");
            cst.setString(1, isbn);
            cst.setString(2, title);
            cst.setString(3, copynum);
            cst.setString(4, author);
            cst.setString(5, shelf);
            cst.setString(6, status);
            cst.setString(7, year);
            cst.setString(8, place);
            
            int confirmation = JOptionPane.showConfirmDialog(rootPane, "Are you sure to add this book?", "Add book", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (confirmation == 0) {
                cst.execute();
                JOptionPane.showMessageDialog(rootPane, "Book added!", "Add book", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }
        }
        catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_saveButtonActionPerformed

